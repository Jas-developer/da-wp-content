             National Budget Circular 542
            </h4>
            <p>
             <span class="fw-semibold text-success">National Budget Circular 542,</span> issued by the Department of Budget and Management on August 29, 2012, r
              eiterates compliance with Section 93 of the General Appropriations Act of FY2012. Section 93 is the 
              Transparency Seal provision, to wit: Sec. 93. Transparency Seal. To enhance transparency and enforce accountability, 
              all national government agencies shall maintain a transparency seal on their official websites. 
              The transparency seal shall contain the following information: (i) the agency’s mandates and functions, names of its officials with their position and designation, and contact information; (ii) annual reports, as required under National Budget Circular Nos. 507 and 507-A dated January 31, 2007 and June 12, 2007, respectively, for the last three (3) years; (iii) their respective approved budgets and corresponding targets immediately upon approval of this Act; (iv) major programs and projects categorized in accordance with the five key results areas under E.O. No. 43, s. 2011; (v) the program/projects beneficiaries as identified in the applicable special provisions; (vi) status of implementation and program/project evaluation and/or assessment reports; and (vii) annual procurement plan, contracts awarded and the name of contractors/suppliers/consultants. The respective heads of the agencies shall be responsible for ensuring compliance with this section. A Transparency Seal, prominently displayed on the main page of the website of a particular government agency, is a certificate that it has complied with the requirements of Section 93. This Seal 
              links to a page within the agency’s website which contains an index of downloadable items of each of the above-mentioned documents.
